package September.Week1.StudyBatch;

public class ExplicitTypeCasting {
    public static void main(String[] args) {
        double d = 10.5;
        float f = (float) d;
        System.out.println(f);

        long l = 324324l;
        int i = (int) l;
        System.out.println(i);

        char c = 'A';
        int i2 = c;
        System.out.println(i2);
    }
}
