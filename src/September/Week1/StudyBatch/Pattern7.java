package September.Week1.StudyBatch;

public class Pattern7 {
    public static void main(String[] args) {
        for (int i = 0; i < 3; i++) {
            for (int j = 3; j > i; j--) {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}

