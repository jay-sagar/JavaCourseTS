package September.Week4.Batch.OOPS;

// encapsulation is process where we wrap the data together into single unit
// the best example of encapsulation is java class
// capsule is the real world example of encapsulation

// Encapsulation is a process of wrapping a data into a single unit like a class in java to hide some sensitive from outside the world
// POJO => Plain old java object
public class Encapsulation {

    private int id;
    private String name;


    Encapsulation(int id, String name) {
        this.id = id;
        this.name = name;
    }
}
